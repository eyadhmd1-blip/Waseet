-- ─── 2. Update init_provider_demo to prefer matching category ─

CREATE OR REPLACE FUNCTION init_provider_demo(p_provider_id UUID)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_demo_id       UUID;
  v_provider_cats TEXT[];
BEGIN
  -- Get provider's registered categories
  SELECT categories INTO v_provider_cats
  FROM providers
  WHERE id = p_provider_id;

  -- Try to pick a demo request matching one of provider's categories
  IF v_provider_cats IS NOT NULL AND array_length(v_provider_cats, 1) > 0 THEN
    SELECT id INTO v_demo_id
    FROM demo_requests
    WHERE category_slug = ANY(v_provider_cats)
    ORDER BY random()
    LIMIT 1;
  END IF;

  -- Fallback: any random demo request
  IF v_demo_id IS NULL THEN
    SELECT id INTO v_demo_id
    FROM demo_requests
    ORDER BY random()
    LIMIT 1;
  END IF;

  INSERT INTO provider_demo_status (provider_id, demo_request_id)
  VALUES (p_provider_id, v_demo_id)
  ON CONFLICT (provider_id) DO NOTHING;
END;
$$;

GRANT EXECUTE ON FUNCTION init_provider_demo(UUID) TO authenticated;
